#Aula de variáveis

#Tipos de Dados/tipos primitivos
nome="Zé Colméia"   #tipo string (str)
idade=26            #tipo inteiro (int)
altura=1.78         #tipo flutuante (float)
animal=True         #tipo booleano (bool)

#Comando print - printa algo no terminal 
print("Hello")
print(nome)
print(idade)
print(altura)
print(animal)

#Comando type - informar o tipo da variável
print(type(nome))
print(type(idade))
print(type(altura))
print(type(animal))

#Comando input - usado para receber dados do usuário, sempre devolve uma string como resultado
pergunta=input("Digite uma mensagem para o Zé Colmeia: ")
print(pergunta)
print("THE END")

